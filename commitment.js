// while
let numb = 1;
while (numb <= 60) {
  console.log(
    numb,
    ": (while) I will invest at least 6 hrs every single day for next 60 days!"
  );
  numb++;
}

// for
for (numb2 = 1; numb2 <= 60; numb2++) {
  console.log(
    numb2,
    ": (for) I will invest at least 6 hrs every single day for next 60 days!"
  );
}
